import numpy as np

# Дефиниране на матрици A и B
A = np.array([[1, 0, 2], [1, 3, 0], [0, 1, 7]])
B = np.array([[2, 7, 2], [4, 1, 3], [5, 0, 9]])

# Проверка дали размерите на матриците съвпадат
if A.shape != B.shape:
    raise ValueError("Матриците A и B трябва да имат еднакви размери!")

# Изчисление на X и Y
X = 6 * (B - A) + 5 * (A - 2 * B) + 2 * (3 * B - 2 * A)
Y = np.dot(A.T, B)

# Ръчно изчислени резултати за сравнение
manual_X = np.array([[-1, 14, -6], [3, -13, 6], [10, -5, -17]])
manual_Y = np.array([[6, 17, 39], [8, 3, 14], [5, 18, 67]])

# Проверка дали X и Y съвпадат с ръчно изчислените
is_X_correct = np.allclose(X, manual_X)
is_Y_correct = np.allclose(Y, manual_Y)

# Отпечатване на резултатите
print("X =\n", X)
print("Y =\n", Y)

# Отпечатване на резултатите от проверките
print("\nСъвпадат ли X с ръчното изчисление? ->", "Да" if is_X_correct else "Не")
print("Съвпадат ли Y с ръчното изчисление? ->", "Да" if is_Y_correct else "Не")
